# A variable is a placeholder for storing data in the computer memory


# VARIABLE NAMING RULES
# 1. Use descriptive names
# 2. Cannot use white spaces and hyphen in between names
# 3. Use underscore
# 4. Use camelCase notation
# 5. Cannot start with a number but can have a number in it
# 6. Cannot use reserved keywords
# 7. Python is case-sensitive


# Multiple Assignment or Unpacking
*x, y, z = 10, 20, 30, 40
print(x)
print(y)
print(z)

a, *b, c = 30, 40, 50, 60, 80
print(a)
print(b)
print(c)


school = "University of Ghana"
print(school)

my_first_phone = "Samsung Galaxy A12"
print(my_first_phone)

# using camelCase notation
myIdeas = "Buy a new diary"
print(myIdeas)

myFavouriteNumber = "My favourite number is 27"
print(myFavouriteNumber)

Sales = "Shoes"
